namespace SKYNET
{
    public partial class frmKey : Form
    {
        public frmKey()
        {
            InitializeComponent();
            KeyPressed.Focus();
            TopMost = true;
        }

        private void KeyPressed_KeyDown(object sender, KeyEventArgs e)
        {
            bool save = true;
            switch (e.KeyData)
            {
                case Keys.Escape:
                    save = false;
                    Close();
                    break;
                case Keys.F1:
                    break;
                case Keys.F2:
                    break;
                case Keys.F3:
                    break;
                case Keys.F4:
                    break;
                case Keys.F5:
                    break;
                case Keys.F6:
                    break;
                case Keys.F7:
                    break;
                case Keys.F8:
                    break;
                case Keys.F9:
                    break;
                case Keys.F10:
                    break;
                case Keys.F11:
                    break;
                case Keys.F12:
                    break;
                default:
                    e.SuppressKeyPress = true;
                    return;
            }
            if (save)
            {
                Settings.Key = e.KeyData.ToString();
            }
            button1.PerformClick();
        }
        protected override void OnActivated(EventArgs e)
        {
            base.OnActivated(e);
            int attrValue = 2;
            DwmApi.DwmSetWindowAttribute(base.Handle, 2, ref attrValue, 4);
            DwmApi.MARGINS mARGINS = default(DwmApi.MARGINS);
            mARGINS.cyBottomHeight = 1;
            mARGINS.cxLeftWidth = 0;
            mARGINS.cxRightWidth = 0;
            mARGINS.cyTopHeight = 0;
            DwmApi.MARGINS marInset = mARGINS;
            DwmApi.DwmExtendFrameIntoClientArea(base.Handle, ref marInset);
        }
    }
}
